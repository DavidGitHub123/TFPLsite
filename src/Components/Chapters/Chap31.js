          <p>What did Mormon leaders do with this alarming information? Did they reevaluate their belief systems? Did they inform church members? They swept it under the rug. They filed away Roberts’ report and sent him away on a mission. After the Secret Meetings of 1922, church leaders recorded their feelings on the veracity of the Book of Mormon, the keystone of their religion.
          </p>
